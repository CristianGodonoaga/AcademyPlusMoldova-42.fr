#include "libft.h"

void	ft_putnbr(int n)
{
	
	if(n < 0)
	{
		ft_putchar('-');	
		n = -n;
	}
	if(n <= '0' && n >= '9')
	{
		n /= 10;
		n %= 10;
	}
	else 
		ft_putchar('0' + n);
}	
