#include "libft.h"

char	*ft_strupcase(char *str)
{
	char	*st;

	st = str;
	while (*str != '\0')
	{
		if (*str >= 'a' && *str <= 'z')
			*str -= 32;
		str++;
	}
	return (st);
}
