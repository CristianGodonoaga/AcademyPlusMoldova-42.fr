#include "libft.h"

size_t	ft_count_digit(long nb)
{
	size_t		i;
	
	i = 0;
	if (nb == 0)
		return (1);
	while (nb != 0)
	{
		nb = nb / 10;
		i++;
	}
	return (i);
}
